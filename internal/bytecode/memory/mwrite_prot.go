// writeTo 将数据写入到可执行内存区中指定的地址, 同时避免 mac 环境下的 permission denied
func writeTo(addr uintptr, data []byte) error {
	pageSize := uintptr(syscall.Getpagesize())
	dataSize := uintptr(len(data))
	m := rawAccess(addr, int(dataSize))
	for p := pageStart(addr); p < addr+dataSize; p += pageSize {
		_, _, errno := syscall.Syscall(syscall.SYS_MPROTECT, p, pageSize, syscall.PROT_READ|syscall.PROT_WRITE)
		if errno != 0 {
			panic(fmt.Errorf("access mem error: %w", errno))
		}
	}
	copy(m, data[:])
	for p := pageStart(addr); p < addr+dataSize; p += pageSize {
		_, _, errno := syscall.Syscall(syscall.SYS_MPROTECT, p, pageSize, syscall.PROT_READ|syscall.PROT_EXEC)
		if errno != 0 {
			panic(fmt.Errorf("access mem error: %w", errno))
		}
	}
	return nil
}

// nolint
// rawAccess 内存数据读取(非线程安全的)
func rawAccess(addr uintptr, length int) []byte {
	return *(*[]byte)(unsafe.Pointer(&reflect.SliceHeader{
		Data: addr,
		Len:  length,
		Cap:  length,
	}))
}

// pageStart page start of memory
func pageStart(addr uintptr) uintptr {
	return addr & ^(uintptr(syscall.Getpagesize() - 1))
}
